            return 0
            ;;
        apt__gen__list__remove__mirror)
            opts=" -h -V  --help --version  <MIRROR>... "
            if [[ ${cur} == -* || ${COMP_CWORD} -eq 2 ]] ; then
                COMPREPLY=( $(compgen -W "${opts}" -- "${cur}") )
                return 0
            fi
            case "${prev}" in
                
                *)
                    COMPREPLY=()
                    ;;
            esac
            COMPREPLY=( $(compgen -W "${opts}" -- "${cur}") )
            return 0
            ;;
        apt__gen__list__reset__mirror)
            opts=" -h -V  --help --version  "
